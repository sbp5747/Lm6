public class Coffe{
    // Fields
    private String name;
    private String type;
    private String phone;
    private int price;

    // Constructor
    public Coffe(String name, String type, String phone, int price) {
        this.name = name;
        this.type = type;
        this.phone = phone;
        this.price = price;
    }

    // Getters and setters
    public String getName() {
        return name;
    }

    public void setName(String Name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(String address) {
        this.price = price;
    }

}